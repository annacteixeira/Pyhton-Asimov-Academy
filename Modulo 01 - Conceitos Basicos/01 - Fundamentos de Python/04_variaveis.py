# Regras para nomeação de variáveis:
    # 01. Usar apenas letras, numeros e underscores
    # 02. Não iniciar nomes de variáveis com números
    # 03. Python é case sensitive. A variável de nome 'x' é diferente da variável de nome 'X'
    # 04. Palavras reservadas como 'if', 'switch', não podem ser usadas como nomes de variáveis

pi = 3.14
raio = 5
raio_ao_quadrado = raio ** 2

area_circulo = pi * raio_ao_quadrado

meu_nome = "Anna"

print(area_circulo)
print(meu_nome)